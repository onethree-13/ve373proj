#define LCD_RESET PORTDbits.RD0

int ResetLCD()
{
    TRISDCLR = 0x0001; // Set LCD_RESET as output
    LCD_RESET = 0;
    DelayMsec(100);
    LCD_RESET = 1;
    DelayMsec(100);
}

int InitLCD()
{
    ResetLCD();
    StartTransfer();
    TransferCommand(0x30); //EXT=0
    TransferCommand(0x94); //Sleep out
    TransferCommand(0x31); //EXT=1
    TransferCommand(0xD7); //Autoread disable
    TransferData(0X9F);    //
    TransferCommand(0x32); //Analog SET
    TransferData(0x00);    //OSC Frequency adjustment
    TransferData(0x01);    //Frequency on booster capacitors->6KHz
    TransferData(0x03);    //Bias=1/11
    TransferCommand(0x20); // Gray Level
    TransferData(0x01);
    TransferData(0x03);
    TransferData(0x05);
    TransferData(0x07);
    TransferData(0x09);
    TransferData(0x0b);
    TransferData(0x0d);
    TransferData(0x10);
    TransferData(0x11);
    TransferData(0x13);
    TransferData(0x15);
    TransferData(0x17);
    TransferData(0x19);
    TransferData(0x1b);
    TransferData(0x1d);
    TransferData(0x1f);
    TransferCommand(0x30); //EXT=0
    TransferCommand(0x75); //Page Address setting
    TransferData(0X00);    // XS=0
    TransferData(0X28);    // XE=159 0x28
    TransferCommand(0x15); //Clumn Address setting
    TransferData(0X00);    // XS=0
    TransferData(0Xff);    // XE=256
    TransferCommand(0xBC); //Data scan direction
    TransferData(0x00);    //MX.MY=Normal
    TransferData(0xA6);
    TransferCommand(0xCA); //Display Control
    TransferData(0X00);    //
    TransferData(0X9F);    //Duty=160
    TransferData(0X20);    //Nline=off
    TransferCommand(0xF0); //Display Mode
    TransferData(0X10);    //10=Monochrome Mode,11=4Gray
    TransferCommand(0x81); //EV control
    TransferData(0x38);    //VPR[5-0]
    TransferData(0x04);    //VPR[8-6]
    TransferCommand(0x20); //Power control
    TransferData(0x0B);    //D0=regulator ; D1=follower ; D3=booste, on:1 off:0
    StopTransfer();
    DelayUsec(100);
    StartTransfer();
    TransferCommand(0xAF); //Display on
    StopTransfer();
}

void lcd_address(int x, int y, int x_total, int y_total)
{
    x = x - 1;
    y = y - 1;
    StartTransfer();
    TransferCommand(0x15); //Set Column Address
    TransferData(x);
    TransferData(x + x_total - 1);
    TransferCommand(0x75); //Set Page Address
    TransferData(y);
    TransferData(y + y_total - 1);
    TransferCommand(0x30);
    TransferCommand(0x5c);
    StopTransfer();
}

void test(UINT8 data1, UINT8 data2)
{
    int i, j;
    StartTransfer();
    TransferCommand(0x30);
    TransferCommand(0xAF); //Display on
    StopTransfer();
    DelayMsec(100);

    lcd_address(1, 1, 256, 21);
    StartTransfer();
    for (i = 0; i < 20; i++)
    {
        for (j = 0; j < 255; j++)
        {
            TransferData(data1);
            TransferData(data2);
        }
    }
    StopTransfer();
}

void clear_screen(int x, int y)
{
    int i, j;
    StartTransfer();
    TransferCommand(0x30);
    TransferCommand(0xAF); //Display on
    StopTransfer();
    DelayMsec(100);

    StartTransfer();
    for (i = 0; i < 0x28; i++)
    {
        for (j = 0; j < 0xFF; j++)
        {
            TransferData(0x00);
        }
    }
    StopTransfer();
}

void LCD_disp(int x, int y, char *dp)
{
    int i, j;
    StartTransfer();
    TransferCommand(0x30);
    TransferCommand(0xAF); //Display on
    StopTransfer();
    DelayMsec(100);
    StartTransfer();

    for (i = 0; i < 0x28; i++)
    {
        for (j = 0; j < 0xFF; j++)
        {
            TransferData(*dp);
            dp++;
        }
    }
    StopTransfer();

    StartTransfer();
    TransferCommand(0x30);
    TransferCommand(0xAF); //Display on
    StopTransfer();
    DelayMsec(100);
}

void LCD_OTP_Write()
{
    StartTransfer();
    TransferCommand(0x30);
    // Extension Command 1
    TransferCommand(0xAE);
    // Display OFF
    StopTransfer();
    DelayMsec(50);
    // Delay 50ms

    StartTransfer();
    TransferCommand(0x39);
    //Enable OTP
    TransferCommand(0XD6);
    TransferData(0x10);
    TransferCommand(0x31);
    // Extension Command 2
    TransferCommand(0xF0);
    // Frame Rate = 73Hz
    TransferData(0x0D);
    TransferData(0x0D);
    TransferData(0x0D);
    TransferData(0x0D);
    TransferCommand(0xE4);
    // OTP Selection Control
    TransferData(0x99);
    TransferCommand(0xE5);
    // OTP Programming Setting
    TransferData(0x0F);
    TransferCommand(0xE0);
    // OTP WR/RD Control
    TransferData(0x20);
    StopTransfer();

    DelayMsec(100);
    // Delay 100ms
    StartTransfer();
    TransferCommand(0xE2);
    StopTransfer();
    // OTP Write
    DelayMsec(100);
    // Delay 100ms

    StartTransfer();
    TransferCommand(0xE1);
    // OTP Control Out
    TransferCommand(0x30);
    // Extension Command 1
    StopTransfer();
}

